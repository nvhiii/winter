package ch1;

public class variableIntro {

    public static void main(String[] args) {

        int chicken = 9000;
        double bacon = 0.1;
        String tractor = "Zetor";

        System.out.println("Chicken:\n" + chicken);
        System.out.println("Bacon (kg):\n" + bacon);
        System.out.println("Tractor:\n" + tractor);
        System.out.println("And finally, a summary:\n" +
                chicken + "\n" +
                bacon + "\n" +
                tractor);

    }

}
